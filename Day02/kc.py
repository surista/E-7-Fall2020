# Soulful grunt
# Void routine
def grunt():
    print("uh-huh uh-huh")


# Chorus
# Void routine
def chorus():
    print("That's the way")
    grunt()
    print("I like it")
    grunt()
    print()


# Hit song
# Void routine and Vapid hit
def hit_single():
    chorus()
    chorus()
    chorus()
    chorus()
    chorus()
    chorus()
    chorus()
    chorus()
    chorus()


hit_single()
